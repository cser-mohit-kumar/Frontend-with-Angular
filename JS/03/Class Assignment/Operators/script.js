// Arithmetic Operators
let a = 10, b = 3;

console.log("Arithmetic:");
console.log("a + b =", a + b);
console.log("a - b =", a - b);
console.log("a * b =", a * b);
console.log("a / b =", a / b);
console.log("a % b =", a % b);
console.log("a ** b =", a ** b);

// Assignment Operators
let x = 5;
x += 2;
x *= 3;

console.log("\nAssignment:");
console.log("x =", x);

// Comparison Operators
console.log("\nComparison:");
console.log("a == b:", a == b);
console.log("a === b:", a === b);
console.log("a != b:", a != b);
console.log("a > b:", a > b);
console.log("a < b:", a < b);

// Logical Operators
console.log("\nLogical:");
console.log("true && false:", true && false);
console.log("true || false:", true || false);
console.log("!true:", !true);

// Increment / Decrement
let i = 5;
console.log("\nIncrement/Decrement:");
console.log("i++:", i++);
console.log("after i++:", i);
console.log("++i:", ++i);

// Ternary Operator
let age = 18;
let result = (age >= 18) ? "Adult" : "Minor";

console.log("\nTernary:");
console.log(result);

// Type Operator
console.log("\nType:");
console.log(typeof a);
console.log(typeof "hello");

// String Operator
let first = "Hello";
let second = "World";

console.log("\nString:");
console.log(first + " " + second);

// Bitwise Operator (basic)
console.log("\nBitwise:");
console.log("5 & 1 =", 5 & 1);
console.log("5 | 1 =", 5 | 1);

